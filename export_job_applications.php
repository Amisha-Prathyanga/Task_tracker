<?php
require_once 'config.php';
requireLogin();

if ($_SERVER['REQUEST_METHOD'] !== 'POST') {
    header('Location: index.php');
    exit();
}

$format = $_POST['format'] ?? 'pdf';
$search = $_POST['search'] ?? '';
$dateFrom = $_POST['date_from'] ?? '';
$dateTo = $_POST['date_to'] ?? '';
$workMode = $_POST['work_mode'] ?? '';
$data = json_decode($_POST['data'], true);

if (empty($data)) {
    header('Location: index.php');
    exit();
}

$username = $_SESSION['username'];
$date = date('Y-m-d H:i:s');

if ($format === 'excel') {
    // Export as Excel (CSV format)
    header('Content-Type: text/csv');
    header('Content-Disposition: attachment; filename="job_applications_report_' . date('Y-m-d_His') . '.csv"');
    
    $output = fopen('php://output', 'w');
    
    // Add report header
    fputcsv($output, ['Job Applications Report']);
    fputcsv($output, ['Generated by: ' . $username]);
    fputcsv($output, ['Date: ' . $date]);
    if ($dateFrom && $dateTo) {
        fputcsv($output, ['Date Range: ' . $dateFrom . ' to ' . $dateTo]);
    } elseif ($dateFrom) {
        fputcsv($output, ['From Date: ' . $dateFrom]);
    } elseif ($dateTo) {
        fputcsv($output, ['To Date: ' . $dateTo]);
    }
    if ($workMode) {
        fputcsv($output, ['Work Mode: ' . $workMode]);
    }
    if ($search) {
        fputcsv($output, ['Search Query: ' . $search]);
    }
    fputcsv($output, ['Total Applications: ' . count($data)]);
    fputcsv($output, []); // Empty row
    
    // Add column headers
    fputcsv($output, ['Job Title', 'Company', 'Date Applied', 'Work Mode', 'Job Description', 'Image']);
    
    // Add data rows
    foreach ($data as $row) {
        fputcsv($output, [
            $row['job_title'],
            $row['company'],
            $row['date_applied'],
            $row['work_mode'],
            $row['job_description'],
            $row['has_image'] ? 'Yes' : 'No'
        ]);
    }
    
    fclose($output);
    exit();
}

if ($format === 'pdf') {
    // Export as PDF using FPDF
    require_once('fpdf/fpdf.php');
    
    class PDF extends FPDF {
        function Header() {
            $this->SetFont('Arial', 'B', 16);
            $this->SetTextColor(156, 39, 176); // Purple color for job applications
            $this->Cell(0, 10, 'Job Applications Report', 0, 1, 'C');
            $this->Ln(5);
        }
        
        function Footer() {
            $this->SetY(-15);
            $this->SetFont('Arial', 'I', 8);
            $this->SetTextColor(128);
            $this->Cell(0, 10, 'Page ' . $this->PageNo(), 0, 0, 'C');
        }
        
        function InfoSection($label, $value) {
            $this->SetFont('Arial', 'B', 10);
            $this->SetTextColor(60);
            $this->Cell(40, 6, $label . ':', 0, 0);
            $this->SetFont('Arial', '', 10);
            $this->SetTextColor(0);
            $this->Cell(0, 6, $value, 0, 1);
        }
        
        function JobApplicationTable($header, $data) {
            // Colors
            $this->SetFillColor(156, 39, 176); // Purple
            $this->SetTextColor(255);
            $this->SetDrawColor(200, 200, 200);
            $this->SetLineWidth(.3);
            $this->SetFont('Arial', 'B', 9);
            
            // Header
            $w = array(50, 45, 30, 25, 115); // Job Title, Company, Date, Work Mode, Description
            for($i = 0; $i < count($header); $i++) {
                $this->Cell($w[$i], 7, $header[$i], 1, 0, 'C', true);
            }
            $this->Ln();
            
            // Data
            $this->SetFillColor(248, 249, 250);
            $this->SetTextColor(0);
            $this->SetFont('Arial', '', 8);
            
            $fill = false;
            foreach($data as $row) {
                // Sanitize and Encode Data
                $jobTitle = iconv('UTF-8', 'ISO-8859-1//TRANSLIT', $row['job_title']);
                $company = iconv('UTF-8', 'ISO-8859-1//TRANSLIT', $row['company']);
                $dateApplied = iconv('UTF-8', 'ISO-8859-1//TRANSLIT', $row['date_applied']);
                $workMode = iconv('UTF-8', 'ISO-8859-1//TRANSLIT', $row['work_mode']);
                $description = iconv('UTF-8', 'ISO-8859-1//TRANSLIT', substr($row['job_description'], 0, 200)); // Limit description length

                // Calculate max height for this row
                $nb1 = $this->NbLines($w[0], $jobTitle);
                $nb2 = $this->NbLines($w[4], $description);
                $nb = max($nb1, $nb2, 1);
                $h = 6 * $nb;
                
                // Check page break
                if($this->GetY() + $h > $this->PageBreakTrigger) {
                    $this->AddPage($this->CurOrientation);
                    
                    // Re-print header
                    $this->SetFillColor(156, 39, 176);
                    $this->SetTextColor(255);
                    $this->SetFont('Arial', 'B', 9);
                    for($i = 0; $i < count($header); $i++) {
                        $this->Cell($w[$i], 7, $header[$i], 1, 0, 'C', true);
                    }
                    $this->Ln();
                    
                    $this->SetFillColor(248, 249, 250);
                    $this->SetTextColor(0);
                    $this->SetFont('Arial', '', 8);
                }
                
                // Draw cells
                $x = $this->GetX();
                $y = $this->GetY();
                
                $this->Rect($x, $y, $w[0], $h, $fill ? 'F' : '');
                $this->MultiCell($w[0], 6, $jobTitle, 0, 'L');
                $this->SetXY($x + $w[0], $y);
                
                $this->Rect($x + $w[0], $y, $w[1], $h, $fill ? 'F' : '');
                $this->MultiCell($w[1], 6, $company, 0, 'L');
                $this->SetXY($x + $w[0] + $w[1], $y);
                
                $this->Rect($x + $w[0] + $w[1], $y, $w[2], $h, $fill ? 'F' : '');
                $this->MultiCell($w[2], 6, $dateApplied, 0, 'C');
                $this->SetXY($x + $w[0] + $w[1] + $w[2], $y);
                
                // Work Mode color
                $color = $this->getWorkModeColor($row['work_mode']);
                $this->SetTextColor($color[0], $color[1], $color[2]);
                $this->Rect($x + $w[0] + $w[1] + $w[2], $y, $w[3], $h, $fill ? 'F' : '');
                $this->MultiCell($w[3], 6, $workMode, 0, 'C');
                $this->SetTextColor(0);
                $this->SetXY($x + $w[0] + $w[1] + $w[2] + $w[3], $y);
                
                $this->Rect($x + $w[0] + $w[1] + $w[2] + $w[3], $y, $w[4], $h, $fill ? 'F' : '');
                $this->MultiCell($w[4], 6, $description, 0, 'L');
                
                $this->Ln($h);
                $fill = !$fill;
            }
            $this->Cell(array_sum($w), 0, '', 'T');
        }

        function NbLines($w, $txt) {
            $cw = &$this->CurrentFont['cw'];
            if($w == 0) $w = $this->w - $this->rMargin - $this->x;
            $wmax = ($w - 2 * $this->cMargin) * 1000 / $this->FontSize;
            $s = str_replace("\r", '', $txt);
            $nb = strlen($s);
            if($nb > 0 && $s[$nb - 1] == "\n") $nb--;
            $sep = -1;
            $i = 0;
            $j = 0;
            $l = 0;
            $nl = 1;
            while($i < $nb) {
                $c = $s[$i];
                if($c == "\n") {
                    $i++;
                    $sep = -1;
                    $j = $i;
                    $l = 0;
                    $nl++;
                    continue;
                }
                if($c == ' ') $sep = $i;
                $l += $cw[$c];
                if($l > $wmax) {
                    if($sep == -1) {
                        if($i == $j) $i++;
                    } else $i = $sep + 1;
                    $sep = -1;
                    $j = $i;
                    $l = 0;
                    $nl++;
                } else $i++;
            }
            return $nl;
        }
        
        function getWorkModeColor($workMode) {
            switch($workMode) {
                case 'Onsite': return array(25, 118, 210); // Blue
                case 'Hybrid': return array(245, 124, 0); // Orange
                case 'Remote': return array(56, 142, 60); // Green
                default: return array(0, 0, 0);
            }
        }
    }
    
    $pdf = new PDF();
    $pdf->AddPage('L'); // Landscape
    
    // Report information
    $pdf->SetFont('Arial', '', 10);
    $pdf->InfoSection('Generated by', $username);
    $pdf->InfoSection('Date', $date);
    if ($dateFrom && $dateTo) {
        $pdf->InfoSection('Date Range', $dateFrom . ' to ' . $dateTo);
    } elseif ($dateFrom) {
        $pdf->InfoSection('From Date', $dateFrom);
    } elseif ($dateTo) {
        $pdf->InfoSection('To Date', $dateTo);
    }
    if ($workMode) {
        $pdf->InfoSection('Work Mode Filter', $workMode);
    }
    if ($search) {
        $pdf->InfoSection('Search Query', $search);
    }
    $pdf->InfoSection('Total Applications', count($data));
    $pdf->Ln(5);
    
    // Table
    $header = array('Job Title', 'Company', 'Date Applied', 'Work Mode', 'Job Description');
    $pdf->JobApplicationTable($header, $data);
    
    $pdf->Output('D', 'job_applications_report_' . date('Y-m-d_His') . '.pdf');
    exit();
}
?>
